#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*p;
	int		i;
	if (s == NULL)
		return NULL;
	i = 0;
	while (s[i])
	{
		i++;
	}
	p = (char *)malloc(sizeof(char) * (i + 1));
	if (p == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		p[i] = s[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
